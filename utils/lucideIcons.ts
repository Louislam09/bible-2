// Function to get icon SVG by name
export const getLucideIcon = (iconName: keyof typeof lucideIcons): string => {
  return lucideIcons[iconName] || '';
};

// Function to create inline icon HTML
export const createInlineIcon = (iconName: keyof typeof lucideIcons, className: string = '', style: string = ''): string => {
  const svg = getLucideIcon(iconName);
  if (!svg) return '';

  return `<span class="${className}" style="${style}">${svg}</span>`;
};

